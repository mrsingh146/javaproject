package com.facebook;

public class pattern1 {
	public static void main(String args[])
	{

      int rows = 4;
       
      System.out.println(" pattern");
       
      for (int i = 1; i <= rows; i++) 
      {
          for (int j = 1; j <= i; j++)
          {
              System.out.print(j+" ");
          }
      	System.out.println();
      }
       
 
  }
}

	


